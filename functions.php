function string_to_ascii($string){
    $ascii = NULL;
 
    for ($i = 0; $i < strlen($string); $i++) 
    { 
    	$ascii += ord($string[$i]); 
    }
 
    return($ascii);
}

function string_to_morse($string, $morsedict){
    $morse = NULL;
 
    for ($i = 0; $i < strlen($string); $i++) 
    { 
    	$morse += $morsedict($string[$i]); 
    }
 
    return($morse);
}

